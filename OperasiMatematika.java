public class OperasiMatematika {

    public static void main(String[] args) {

        int i = 37;
        int j = 42;
        double x = 27.475;
        double y = 7.22;
        // penjumlahan dibagian int i dan j
        System.out.println(i + " + " + j+ "= " + (i + j));
        System.out.println(x + " + " + y+ "= " + (x + y));
        //pengurangan
        System.out.println(x + " - " + y+ "= " + (i - j));
        System.out.println(i + " - " + j+ "= " + (i - j));
        //perkalian
        System.out.println(i + " * " + j+ "= " + (i * j));
        System.out.println(x + "* " + y+ "= " + (x * y));
        //pembagian
        System.out.println(i + " / " + j+ "= " + (i / j));
        System.out.println(x + " / " + y+ "= " + (x / y));
        //sisa bagi
        System.out.println(i + " % " + j+ "= " + (i % j));
        System.out.println(x + " % " + y+ "= " + (x % y));

    }
}