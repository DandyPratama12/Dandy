public class PenjumlahanRataRata {
   public static void main(String[] args) {
        
    int ipa    = 50;
    int ips    = 60;
    int mtk    = 70;
    int ppkn   = 80;
    int penjas = 90;
    int hasil = ipa + ips + mtk +  ppkn + penjas;
    System.out.println("Hasil Penjumlahan: " + hasil);

    int nilaiRataRata = hasil / 5;
    System.out.println("Hasil Rata Rata : " + nilaiRataRata);
    
    }
}