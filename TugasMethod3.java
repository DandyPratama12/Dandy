public class TugasMethod3 {
    public static void main(String[] args) {
        int angka1 = 5;
        int angka2 = 7;

        int Penjumlahan = hasiljumlah(angka1, angka2);
        System.out.println("Hasil Penjumlahan dari "+ angka1 + " + "+ angka2 + " adalah "+ Penjumlahan);
    }
    public static int hasiljumlah(int angka1, int angka2){
        int hasil = angka1 + angka2;
        return hasil;
    }
}
