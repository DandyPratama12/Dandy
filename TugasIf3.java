public class TugasIf3 {
    public static void main(String[] args) {

        int p  = 30;
        int n  = 24;
        int d  = 20;

        switch (p){
            case 30 :
                System.out.println("Suhu Cuaca Ketika Panas : " + p + "°C");
                break;
        }
        switch (n){
            case 24 :
                System.out.println("Suhu Cuaca Ketika Normal : " + n + "°C");
                break;
        }
        switch (d){
            case 20:
                System.out.println("Suhu Cuaca Ketika Dingin : " + d + "°C");
                break;
        }
    }
}
